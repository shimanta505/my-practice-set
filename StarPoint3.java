public class StarPoint3 {
    public static void main (String[]args){
        for (int i = 0;i <= 4;i++){

            for (int w = 4;w > i;w--){
                System.out.print("*");
            }
            System.out.println("*");

        }

        for (int j = 0;j <= 4;j++){

            for (int k = 0;k  <= j;k++){
                System.out.print("*");
            }

            System.out.println("*");


        }



    }

}
